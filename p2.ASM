 org 100h  ;Программа начинается с адреса 100h

     mov bx,array ;Помещаем в BX адрес начала массива
     mov di,n	  ;Помещаем в DI адрес n
     mov cx,[di]  ;CX = n
     mov al,0
     mov ax,0000  ;В регимтрне AX новое значение элемента 170

      ;Цикл по перобору ячеек диопазона

 cycle:
     cmp [bx],al  ;Проверяем если значение >=0
     jbe minus	  ;Если условие (DI<=0) выполнелось то переходим в minus
     add ax,[bx+1]
     inc ax
     inc bx	  ;Наращевание адреса диопазона
     loop cycle    ;CX<-CX-1 , после чего, усли CX<>0, переход на адрес cycle

 minus:
     add ax,[bx-1]



 mov ax,4C00h	;Завершение работы
