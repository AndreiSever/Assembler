   mvi c,5	   ; c = 5
   lxi h,arr	   ; помещаем адрес на массив в пару HL

 plus:
    jnc hlt	    ;Если шаг(признак) c = 0 Выход
    mov a,m	    ;помещаем первый элемент массива в аккумулятор a = arr[d]
    ora a
    jp minus	    ;Перейти если плюс
    add a	    ;Увеличиваем значение аккумулятора на 1
    mov m,a	    ;Заменяем элемент массива
    inx h	    ;переходим к следующему элементу массива,	arr[d+1]
    cdr c	    ;Уменьшаем кол-во шагов
    jnz plus	    ;Если с <> 0 то переходим в plus
 

 minus:
    jnc hlt	    ;Если шаг c = 0 Выход
    sub a	    ;Уменьшаем значение аккумулятора на 1
    mov m,a	    ;Заменяем элемент массива
    inx h	    ;переходим к следующему элементу массива,	arr[d+1]
    dcr c	    ;Уменьшаем кол-во шагов
    jnz plus	    ;Если с <> 0 то переходим в plus
 
 
 hlt		 ; выходим из программы

 arr db 02h, 01h, 03h, 04h, 05h ;заданный массив