 use16	  ;Генерировать 16-битный код
 org 100h  ;Программа начинается с адреса 100h

   mov BX,array 	;BX = адрес массива
   movzx CX,[length]	;CX = длина массива
   cwd			;DX = 0

    ;Цикл по перобору ячеек диопазона

   plus:
     jcxz exit	  ;Если счетчик CX = 0 то переход на exit
     mov AL,[BX]  ;Помещаем в AL значение элемента массива BX чтобы потом изменить значение
     ;or
     or AL,0	  ;Положительный знак устанавоивается
     jns minus	  ;Переход на minus если знак = 0
     add AL,1	  ;К отрицателному числу прибавляем 1
     mov [BX],AL  ;Заменяем старое значение BX на новое из AL
     inc BX	  ;Следующий элемент массива
     loop plus	  ;CX<-CX-1 , после чего, усли CX<>0, переход на адрес plus


   minus:
     jcxz exit	  ; Если счетчик CX = 0 то переход на exit
     ;sub
     sub AL,1	  ;От положительного числа отнять 1
     mov [BX],AL  ;
     inc BX	  ;Увеличить значение ячеки BX на 1
     loop plus	  ;CX<-CX-1 , после чего, усли CX<>0, переход на адрес plus

   exit:
     mov ax,4C00h   ;Завершение работы
     int 21h

length db 5	   ;Дина массива
array  db 1,-97,5,-3,97   ;Массив